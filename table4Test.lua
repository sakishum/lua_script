-- 注意:构造式的第一个元素的索引为1, 而非0
days = {"Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sta", }
print(days[1])

-- Lua允许在方括号之间，显式地用一个表达式来初始化索引值
days = {[0] = "Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sta", }
print(days[0])

-- [[***********************************************************]]
-- 警告:不推荐在Lua中以0作为数组的起始索引, 因为大多数内建函数都以
-- 假设数组起始值索引为0, 如果遇到以1 为起始索引的数组，它们就无法
-- 处理了.
-- [[***********************************************************]]
